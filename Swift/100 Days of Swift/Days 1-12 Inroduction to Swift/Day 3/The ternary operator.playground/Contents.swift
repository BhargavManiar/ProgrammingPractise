import UIKit

/// Ternary operator works with three values. Checks condition in first value, if true returns second value else the third one

let firstCard = 11
let secondCard = 10
print(firstCard == secondCard ? "Cards are the same" : "Cards are different")

// The same code with regular if and else statemetns
if firstCard == secondCard {
    print("Cards are the same")
} else {
    print("Cards are different")
}
